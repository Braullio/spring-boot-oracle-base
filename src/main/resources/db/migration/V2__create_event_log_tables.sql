CREATE TABLE event_logs (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    routing_key VARCHAR2(255 CHAR) NOT NULL,
    event_type VARCHAR2(255 CHAR) NOT NULL,
    payload CLOB,
    sent NUMBER(1) DEFAULT 0 NOT NULL,
    error NUMBER DEFAULT 0 NOT NULL,
    timestamp TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);
